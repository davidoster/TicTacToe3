package models;

/**
 *
 * @author mac
 */
public class Symbol {
    private String symbol;
    
    public Symbol(){};
    

    public Symbol(String symbol) {
        this.symbol = symbol;
    }

    public String getSymbol() {
        return symbol;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }

    @Override
    public String toString() {
        return "Symbol{" + "symbol=" + symbol + '}';
    }
    
    
}
